package myproject;

public class Stringbuffer {

	public static void main(String[] args) {
		
		String name = "kalai";
		System.out.println(name.concat("vani"));
		System.out.println(name);
		
		
		
		StringBuffer sb=new StringBuffer("kalai");  
		   //append() method
		sb.append("vani");
		System.out.println(sb); 
		System.out.println(sb);
		  
		 
	}

}
