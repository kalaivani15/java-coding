package myproject;

public class Datatypes {

	public static void main(String[] args) {
		byte a= 100;    //byte -128 to 127
		short b = 5000;  //short -32768 to 32767
		int c = 10000;      // int -2147483648 to 2147483647
		double d =19.99d;     // double 1.7e−308 to 1.7e+308
		long e =15000000000L;   //  long -9223372036854775808 to 9223372036854775807
		float f =15.5f;   //  float 3.4e−038 to 3.4e+038
		char ch = 'x' ;  //single value
		char k = 65;       //ASCII value
		boolean java = true;   //True or False
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(java);
		System.out.println(k);
		System.out.println(ch);

	}

}
